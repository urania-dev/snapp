<script lang="ts">
	import Card from '$lib/ui/card.svelte';
	import Chart from '$lib/ui/chart.svelte';
	import Icon from '$lib/ui/icon.svelte';
	import { _ } from 'svelte-i18n';
	let { data } = $props();
	const get_dates = () => {
		let dataset: number[] = [];
		let labels: string[] = [];

		const days = Math.abs(
			Math.floor((data.end.getTime() - data.start.getTime()) / 1000 / 60 / 60 / 24)
		);

		const date = data.start;

		for (let i = 0; i <= days; i++) {
			const dateString = date.toISOString().split('T')[0];

			if (dateString in data.metrics) {
				dataset.push(data.metrics[dateString]);
			} else {
				dataset.push(0);
			}
			labels.push(dateString);

			date.setDate(date.getDate() + 1);
		}
		return {
			dataset,
			labels
		};
	};
	const chartData = $derived(get_dates());

	import Map from '$lib/ui/map.svelte';
	import Select from '$lib/ui/select.svelte';
	import { translateCountry } from '$lib/utils/language';
	import { queryParameters } from 'sveltekit-search-params';

	const params = queryParameters({
		countryID: true,
		os: true,
		device: true,
		browser: true
	});

	let country = $state<string>();
	let dataset = $derived(data.countries);
</script>

<svelte:head>
	<title>{$_('appname')} | {$_('menu.metrics')}</title>
	<meta
		name="description"
		content="Self-hostable Open Source Url Shortner. Discover more at https://github.com/urania-dev/snapp"
	/>
</svelte:head>
<div class="flex w-full md:grow flex-col p-4">
	<div class="mx-auto flex h-full w-full max-w-5xl flex-col gap-4">
		<div class="flex h-8 items-center gap-2">
			<Icon ph="presentation-chart" size={36} />
			<h2 class="text-2xl font-bold">{$_('menu.metrics')}</h2>
		</div>
		<div class="flex w-full flex-col gap-4 lg:flex-row">
			{#key [data.start, data.end]}
				<Card css={{ card: 'flex-row w-full' }}>
					<Chart
						label={$_('snapps.fields.hit')}
						total_count={data.totalVisits}
						css={{ container: 'h-64' }}
						data={chartData}
						start={data.start}
						end={data.end}
					></Chart>
				</Card>
			{/key}
			<Card css={{ card: 'lg:max-w-sm gap-4' }}>
				<h4 class="mb-auto flex w-full items-center gap-2 text-lg font-semibold">
					<Icon ph="funnel"></Icon>
					<span>
						{$_('metrics.filters')}
					</span>
				</h4>
				<Select
					label={$_('metrics.origin')}
					items={[
						{ id: 'world', value: $_('globals.world') },
						...data.countries
							.filter((i) => i.id !== null && i.name !== null)
							.map((i) => ({ id: i.id!, value: translateCountry(data.lang, i.id!) }))
					]}
					placeholder={$_('globals.world')}
					actions={{
						select: (e) => {
							e.preventDefault();
							e.stopPropagation();
							const idx = e.currentTarget.dataset.idx;
							if (!idx) return;
							country = idx === 'world' ? $_('globals.world') : translateCountry(data.lang, idx);
							params.countryID = idx === 'world' ? 'World' : translateCountry('en', idx);
						}
					}}
					name="field"
					bind:value={country}
				/>
				<Select
					label={$_('metrics.browser')}
					items={[
						{ id: 'all', value: $_('globals.all') },
						...data.browsers
							.filter((i) => i.id !== null && i.name !== null)
							.map((i) => ({ id: i.id!, value: i.name || 'not-set' }))
					]}
					placeholder={$_('globals.all')}
					actions={{
						select: (e) => {
							e.preventDefault();
							e.stopPropagation();
							const idx = e.currentTarget.dataset.idx;
							if (!idx) return;
							params.browser = idx;
						}
					}}
					name="field"
					bind:value={params.browser}
				/>
				<Select
					label={$_('metrics.os')}
					items={[
						{ id: 'all', value: $_('globals.all') },
						...data.oss
							.filter((i) => i.id !== null && i.name !== null)
							.map((i) => ({ id: i.id!, value: i.name || 'not-set' }))
					]}
					placeholder={$_('globals.all')}
					actions={{
						select: (e) => {
							e.preventDefault();
							e.stopPropagation();
							const idx = e.currentTarget.dataset.idx;
							if (!idx) return;
							params.os = idx;
						}
					}}
					name="field"
					bind:value={params.os}
				/>
				<Select
					label={$_('metrics.device')}
					items={[
						{ id: 'all', value: $_('globals.all') },
						...data.devices
							.filter((i) => i.id !== null && i.name !== null)
							.map((i) => ({ id: i.id!, value: i.name || 'not-set' }))
					]}
					placeholder={$_('globals.all')}
					actions={{
						select: (e) => {
							e.preventDefault();
							e.stopPropagation();
							const idx = e.currentTarget.dataset.idx;
							if (!idx) return;
							params.device = idx;
						}
					}}
					name="field"
					bind:value={params.device}
				/>
			</Card>
		</div>

		<Card css={{ card: 'w-full aspect-[4/2] lg:aspect-[4/1.5] h-full' }}>
			<Map theme={data.theme} data={dataset}></Map>
		</Card>
	</div>
</div>
